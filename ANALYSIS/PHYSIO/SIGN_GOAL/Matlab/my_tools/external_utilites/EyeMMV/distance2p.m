%distance between 2 points
%point 1(x1,y1)
%point 2(x2,y2)

function distance2p=distance2p(x1,y1,x2,y2)
dx=x2-x1;
dy=y2-y1;
distance2p=sqrt((dx^2)+(dy^2));
end